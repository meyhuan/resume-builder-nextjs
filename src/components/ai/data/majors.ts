/**
 * Searches majors by keyword, returns matching entries.
 */
export function searchMajors(keyword: string): string[] {
  if (!keyword.trim()) return [];
  const kw = keyword.trim().toLowerCase();
  return MAJORS.filter((m) => m.toLowerCase().includes(kw));
}
